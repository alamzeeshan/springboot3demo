package com.league.t20.model.teamlanding;

public class SocialDetails {
	
	
	private String social_platform_id;
	
	private String social_platform_name;
	
	private String social_platform_url;

	public String getSocial_platform_id() {
		return social_platform_id;
	}

	public void setSocial_platform_id(String social_platform_id) {
		this.social_platform_id = social_platform_id;
	}

	public String getSocial_platform_name() {
		return social_platform_name;
	}

	public void setSocial_platform_name(String social_platform_name) {
		this.social_platform_name = social_platform_name;
	}

	public String getSocial_platform_url() {
		return social_platform_url;
	}

	public void setSocial_platform_url(String social_platform_url) {
		this.social_platform_url = social_platform_url;
	}
	
	
	

}
